package com.example.apsapp;

public class CadastroCurso {

    private Integer idCurso;
    private String nomeCurso;
    private String turnoCurso;
    private String turmaCurso;
    private String salaCurso;
    private String horarioCurso;

    public Integer getIdCurso() {
        return idCurso;
    }

    public void setIdCurso(Integer idCurso) {
        this.idCurso = idCurso;
    }

    public String getNomeCurso() {
        return nomeCurso;
    }

    public void setNomeCurso(String nomeCurso) {
        this.nomeCurso = nomeCurso;
    }

    public String getTurnoCurso() {
        return turnoCurso;
    }

    public void setTurnoCurso(String turnoCurso) {
        this.turnoCurso = turnoCurso;
    }

    public String getSalaCurso() {
        return salaCurso;
    }

    public void setSalaCurso(String salaCurso) {
        this.salaCurso = salaCurso;
    }

    public String getHorarioCurso() {
        return horarioCurso;
    }

    public void setHorarioCurso(String horarioCurso) {
        this.horarioCurso = horarioCurso;
    }

    public String getTurmaCurso() {
        return turmaCurso;
    }

    public void setTurmaCurso(String turmaCurso) {
        this.turmaCurso = turmaCurso;
    }
}
